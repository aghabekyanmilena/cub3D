#include "../includes/cub3d.h"

int	val_player(char c)
{
	if (c == 'n' || c == 'N'
		|| c == 'w' || c == 'W'
		|| c == 's' || c == 'S'
		|| c == 'e' || c == 'E')
		return 1;
	else
		return 0;
}
